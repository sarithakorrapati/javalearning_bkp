package com.learning.automation.javalearning1;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int firstValue = 20;
		int secondValue = 6;
		//int result = firstValue + secondValue;
		System.out.println("Substraction  of " + firstValue +" & " + secondValue + " is: " + (firstValue + secondValue));
		System.out.println("Substraction of " + firstValue +" & " + secondValue + " is: " + (firstValue - secondValue));
		System.out.println("Multiplication of " + firstValue +" & " + secondValue + " is: " + (firstValue * secondValue));
		System.out.println("Division of " + firstValue +" & " + secondValue + " is: " + (firstValue / secondValue));
		System.out.println("Modulo of " + firstValue +" & " + secondValue + " is: " + (firstValue % secondValue));
	
	}
	
}
